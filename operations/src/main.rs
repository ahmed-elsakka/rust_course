fn main() {

    // Arithmetic Operations
    let sum = 5 + 9; // 14
    let diff = 9 - 5; // 4
    let product = 4 * 3; // 12
    let div = 10 / 2; // 5
    let remainder = 10 % 3; // 1 

    // Comparison Operations
    let is_equal: bool = sum == diff; // false
    let is_not_equal = product != div; // true
    let is_greater = remainder >= div; // false
    let is_less = remainder <= div; // true

    // Assignment Operations
    let mut x = 5;
    x = x + 10;
    x += 10;
    x *= 10;

}
